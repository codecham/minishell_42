#include "../../includes/minishell.h"

int	ft_autorized_char(char c)
{
	if (ft_isprint(c) == 0)
		return (-1);
	if (c == '!' || c == '#' || c == '*' || c == '&'
		|| c == '(' || c == ')' || c == ';' || c == '`')
		return (-1);
	return (0);
}
